# relational operators are commonly used in boolean expressions
# to create boolean values

# checking object values for equality

print(5 == 5)

# in this expression py will first evaluate the first operand, determine its type
# then will try to type case string to integer, and if succeed will evaluate the 
# expression and return boolean value

print(5 == "5")

print("a" == "A")

# the same logic apply here as well
print(5 == 5.0)

# check for object inequality

print(5 != 5.0)

# using comparison operators

print(5 > 2)
print(5 >= 2)
print(5 < 2)
print(5 <= 2)